public class JefePlantaPojo {
    
    private String rutJP;
    private String nombreJP;
    private String apellidoJP;
    private String telefonoJP;
    private String emailJP;
    private String newPass;
    private String confNewPass;
    private String antiguoPass;
    
    
    public JefePlantaPojo(){}

    public String getRutJP() {
        return rutJP;
    }

    public void setRutJP(String rutJP) {
        this.rutJP = rutJP;
    }

    public String getNombreJP() {
        return nombreJP;
    }

    public void setNombreJP(String nombreJP) {
        this.nombreJP = nombreJP;
    }

    public String getApellidoJP() {
        return apellidoJP;
    }

    public void setApellidoJP(String apellidoJP) {
        this.apellidoJP = apellidoJP;
    }

    public String getTelefonoJP() {
        return telefonoJP;
    }

    public void setTelefonoJP(String telefonoJP) {
        this.telefonoJP = telefonoJP;
    }

    public String getEmailJP() {
        return emailJP;
    }

    public void setEmailJP(String emailJP) {
        this.emailJP = emailJP;
    }

    public String getNewPass() {
        return newPass;
    }

    public void setNewPass(String newPass) {
        this.newPass = newPass;
    }

    public String getConfNewPass() {
        return confNewPass;
    }

    public void setConfNewPass(String confNewPass) {
        this.confNewPass = confNewPass;
    }

    public String getAntiguoPass() {
        return antiguoPass;
    }

    public void setAntiguoPass(String antiguoPass) {
        this.antiguoPass = antiguoPass;
    }
    
    
    
}
